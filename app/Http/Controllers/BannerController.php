    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();
        if($request->banner1){

            $banner1 = $request->file('banner1');
            $tujuan = 'cover';
            $namaFile =  time() . '1' . '.' . $banner1->getClientOriginalExtension();

            $banner1->move($tujuan,$namaFile);

            $data['banner1'] = $namaFile;
        }

        if($request->banner2){

            $banner2 = $request->file('banner2');
            $tujuan = 'cover';
            $namaFile =  time() . '.' . $banner2->getClientOriginalExtension();

            $banner2->move($tujuan,$namaFile);

            $data['banner2'] = $namaFile;
        }
        Banner::create($data);
        return redirect()->back();
    }

